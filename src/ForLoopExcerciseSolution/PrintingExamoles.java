package ForLoopExcerciseSolution;

public class PrintingExamoles {

	public static void main(String[] args) {
		int a=2;
		int b=3;
		int c=4;
		System.out.println(a+b+"c");
		System.out.println(a+b+c);
		System.out.println("a"+b+c);
		System.out.println(2+3+"4");
		System.out.println(2+3+4);
		System.out.println(2+"3"+4);
		System.out.println("2"+3+4);

	}

}
